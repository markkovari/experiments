// Fastify app that embeds the auth-guard component IN-PROCESS via jco.
//
// No network hop to wasmCloud: the WIT contract runs inside this Node process.
// jco transpiled auth_guard.wasm -> ./gen; we supply the keyvalue + config
// host imports (see src/shims/*), jco supplies the rest of WASI.
//
//   register/login/me/logout call the component's exported functions directly.
//   guarded routes call authorizer.authorize(token, {target, action}).
//
// The component's `result<_, auth-error>` becomes: return value on ok, THROW on
// err. The thrown value is the auth-error variant, e.g. { tag: "expired" } or
// { tag: "insufficient-scope", val: {...} }.

import Fastify from "fastify";
// Generated by `jco transpile` (run `npm run transpile` first).
import { accounts, authorizer, session } from "../gen/auth_guard.js";

interface Principal {
  subject: string;
  tenant: string;
  roles: string[];
  scopes: string[];
}
interface AuthErr {
  tag: string;
  val?: unknown;
}

const PORT = Number(process.env.PORT ?? 3001);
const app = Fastify({ logger: true });

// WIT u64 fields (expires-at / expires-in) arrive as JS BigInt, which
// JSON.stringify can't serialize. Teach Fastify's serializer to coerce them.
app.setReplySerializer((payload) =>
  JSON.stringify(payload, (_k, v) => (typeof v === "bigint" ? Number(v) : v)),
);

// Map a thrown auth-error variant to an HTTP status + code.
function statusFor(e: AuthErr): [number, string] {
  switch (e.tag) {
    case "invalid-credentials": return [401, "invalid_credentials"];
    case "already-exists": return [409, "already_exists"];
    case "insufficient-scope": return [403, "insufficient_scope"];
    case "expired": return [401, "expired"];
    case "invalid-token": return [401, "invalid_token"];
    case "unknown-tenant": return [403, "unknown_tenant"];
    case "malformed": return [400, "malformed"];
    case "backend-unavailable": return [503, "backend_unavailable"];
    default: return [500, "internal"];
  }
}

function bearer(auth?: string): string | undefined {
  return auth?.startsWith("Bearer ") ? auth.slice(7).trim() : undefined;
}

// Wrap a direct component call: translate thrown auth-errors into HTTP replies.
function guarded<T>(reply: import("fastify").FastifyReply, fn: () => T): T | undefined {
  try {
    return fn();
  } catch (e) {
    // jco maps a WIT `result` error to a thrown value. For our variant
    // `auth-error` it is `{ tag, val? }` (possibly wrapped in a ComponentError
    // with `.payload`). Normalize, then map to HTTP.
    const err = (e && typeof e === "object" && "payload" in e ? (e as { payload: unknown }).payload : e) as AuthErr;
    if (!err || typeof err.tag !== "string") {
      app.log.error(e, "non-auth throw from component");
      reply.code(500).send({ error: "internal" });
      return undefined;
    }
    const [code, msg] = statusFor(err);
    reply.code(code).send({ error: msg });
    return undefined;
  }
}

app.post("/auth/register", async (request, reply) => {
  const { email, password, tenant } = request.body as Record<string, string>;
  const p = guarded(reply, () => accounts.register(email, password, tenant ?? "") as Principal);
  if (p) reply.code(201).send(p);
});

app.post("/auth/login", async (request, reply) => {
  const { email, password, tenant } = request.body as Record<string, string>;
  return guarded(reply, () => accounts.login(email, password, tenant ?? ""));
});

app.get("/auth/me", async (request, reply) => {
  const token = bearer(request.headers.authorization);
  if (!token) return reply.code(401).send({ error: "missing_bearer_token" });
  return guarded(reply, () => authorizer.introspect(token));
});

app.post("/auth/logout", async (request, reply) => {
  const token = bearer(request.headers.authorization);
  if (!token) return reply.code(401).send({ error: "missing_bearer_token" });
  guarded(reply, () => session.revoke(token));
  if (!reply.sent) reply.code(204).send();
});

// Guarded business route — RBAC check happens inside the component.
app.get("/orders", async (request, reply) => {
  const token = bearer(request.headers.authorization);
  if (!token) return reply.code(401).send({ error: "missing_bearer_token" });
  const principal = guarded(
    reply,
    () => authorizer.authorize(token, { target: "orders", action: "read" }) as Principal,
  );
  if (principal) return { orders: [{ id: 1, item: "widget" }], viewer: principal.subject };
});

try {
  await app.listen({ port: PORT, host: "0.0.0.0" });
} catch (err) {
  app.log.error(err);
  process.exit(1);
}
